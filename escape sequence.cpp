//C program to demonstrate examples of escape sequences
#include<stdio.h>
int main()
{
    printf("Hello\nWorld\n");
    printf("Hello\tWorld\n");
    printf("Hello\bWorld\n");
    printf("Hello\rWorld\n");
    printf("Hello\fWorld\n");
    printf("Hello\\World\n");
    printf("Hello\"World\n");
    printf("Hello\'World\n");
    return 0;
}
